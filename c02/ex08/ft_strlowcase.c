#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int	i;
	int	c;

	i = 0;
	while (str[i] != '\0')
	{
		c = *(str + i);
		if (c >= 65 && c <= 90)
			str[i] += 32;
		i++;
	}
	return (str);
}
